/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  // ++- or --+
  int sum = x + y;
  int var1 = x >> 31;
  int var2 = y >> 31;
  int var3 = sum >> 31;
  int ret1 = !var1 & !var2 & var3;
  int ret2 = var1 & var2 & !(var3);
  return !(ret1 | ret2);
}
/* 
 * rempwr2 - Compute x%(2^n), for 0 <= n <= 30
 *   Negative arguments should yield negative remainders
 *   Examples: rempwr2(15,2) = 3, rempwr2(-35,3) = -3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int rempwr2(int x, int n) {
  int s=x>>31;//x为正数就是全0,x为负数就是全1
  int mask;
  int ans;
  x=(x^s)+(s&0x1);//x为负数的时候就是按位取反+1;
  mask=(1<<n)+(~0);
  ans=((x&mask)^s)+(s&0x1);//同上
  return ans;
}

/* 
 * isLess - if x < y  then return 1, else return 0 
 *   Example: isLess(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLess(int x, int y) {
  int ret1 = ((x + ~y + 1) >> 31) & 1;
  int var1 = x >> 31;
  int var2 = y >> 31;
  int ret2 = ~var1 & var2 & 1;
  int ret3 = var1 & ~var2 & 1;
  return (ret1 | ret3) & !ret2;
}
/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
  int neg = ~x + 1;
  return (~(x >> 31) & x) + (~(neg >> 31) & neg);
}
/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int isPower2(int x) {
  int mask_x = x & (~x + 1);
  return !((x ^ mask_x) | (!x) | (x >> 31));
}
